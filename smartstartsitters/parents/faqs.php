      <h1 class="header">Smart Start Sitter Frequently Asked Questions<br />For Parents</h1>

      <p><span class="subhead_blue">Boutique Service</span></p>

      <p><b><i>Q. How much control do I have over who will look after my children?</i></b><br />

	A. Although Smart Start Sitters carefully vets sitters in our Boutique
	Placement Service, we offer you the opportunity to know more by
	providing a synopsis of the sitter's resume and references.</p>

      <p><b><i>Q. Can I interview a potential sitter?</i></b><br />

        A. Yes, for part-time and full-time nannies. However, we ask
        that you keep the interview to no more than 1 hour. You may
        interview up to 2 sitters before making your final
        decision.</p>

      <p><b><i>Q. Can your sitter "train" with my current sitter who
      is leaving the position?</i></b><br />

	A. Your child's comfort with a Smart Start sitter is our
	number one priority. In keeping with our "Child First" policy,
	we offer "sitter training" for families whose current sitter
	is leaving, whether it's a Smart Start Sitter or your current
	sitter. However, you are still responsible for paying the
	trailing sitter, as well as the training sitter, if both are
	from our service.</p>

      <p><b><i>Q. If I have a particularly good relationship with a
      sitter, may I book with them directly?</i></b><br />

	A. All on-call and temporary bookings are done through the
	office. We will do our best to enable these relationships, but
	please call or email the office to book a particular sitter in
	order to avoid scheduling conflicts.</p>

      <p><b><i>Q. What if I expect to be home later than the booking
      time?</i></b><br />

	A. This applies to on-call and temporary sitters. Please let
	the sitter know as soon as possible if you will be later than
	expected. There is no guarantee the sitter will be able to
	accommodate you if this is a last minute request. If you are
	more than 15 minutes over your booking time, please pay the
	sitter an extra half hour of sitting. If you are more than 30
	minutes or more over your booking time, please pay the sitter
	an extra hour of sitting.</p>

      <p><b><i>Q. What if I only need a sitter for 2 hours?</i></b><br />

	A. We have a 3 hour minimum per sitting. You may use only 2
	hours, but the sitter must be paid the minimum.</p>

      <p><b><i>Q. Do I have to pay cab fare to the sitter?</i></b><br />

	A. After 9:00p.m. we require $10.00 in cash to the sitter to help her
	get home safely.</p>

      <p><b><i>Q. Are the prices listed on your website the same for
      every sitting situation?</i></b><br />

	A. This prices listed on the website are for residential
	sitting here in NYC. If you need a sitter for your hotel
	stay, special event, overnights, travel with family, or child
	with special needs, please call us for pricing.</p>

      <p><b><i>Q. Do you take credit cards?</i></b><br />

	A. Yes. However, we only accept Visa and Master Card.</p>

      <p><b><i>Q. Do you take personal checks?</i></b><br />

	A. No, we only accept credit cards for booking fees. </p>

      <p>&nbsp;</p>
      <p><span class="subhead_blue">Online Service</span></p>

      <p><b><i>Q. Do you have online caregivers available to work in
      my area?</i></b><br />

	A. We are working hard to bring caregivers to every family
	online, no matter where they live. Currently we serve the
	Greater New York City area, but are continuing to expand our
	coverage. Please let us know where you reside, so we can work
	to get available care in your area.</p>

      <p><b><i>Q. I didn't find a caregiver that met my needs from
      your online listings. What do I do now?</i></b><br />

	A. Our experience shows not every family needs the same type
	of care, so we feature caregivers with a diverse range of
	skills and experience. If you haven't found an appropriate
	caregiver through the standard sitter listing, you may want to
	consider upgrading your membership to access caregivers with a
	higher level of qualifications and skills for your specific
	needs.</p>

      <p><b><i>Q. I don't have time to use your online search for a
      caregiver. Can I use your boutique service
      instead?</i></b><br />

	A. Yes! If you don't have the time or inclination to find a
	caregiver online, contact us about our Boutique Service - you
	can even order a caregiver on our website. We take the hassle
	out of finding an Elite Caregiver customized to your
	needs!</p>

      <p>&nbsp;</p>
      <p><span class="subhead_blue">Verification Services</span></p>

      <p><b><i>Q. Can I order a background check for my online sitter
      through you?</i></b><br />

	A. Yes, we offer criminal background checks for any online
	sitter you choose.  The cost for a criminal check is
	$14.99. Click here to learn more.</p>

      <p><b><i>Q. Can I have my sitter vetted by you even though I
      hired them through the online service?</i></b><br />

	A. Yes. Smart Start Sitters can vet any sitter you select for
	a $19.99 service fee. Click here to learn more.</p>
